package day08_IfStatement;

public class example1 {

    public static void main(String[] args) {

        System.out.println(3+2+"+"+4+5);          // 5+45
        System.out.println("+"+4+5);              // +45
        System.out.println("+"+(4+5));            // +9
        System.out.println(4+5+4*5/5+2+"+");      // 15+
        System.out.println(4+5+4*5/5+2+"+"+6/3);  // 15+2
        System.out.println(4+5+4*5/5+2+"+"+6*3);  // 15+18
        System.out.println(4+5+4*5/5+2+"+"+6+3);  // 15+63
        System.out.println(4+5+4*5/5+2+"+"+6%3);  // 15+0

    }

}
