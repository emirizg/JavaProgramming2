package day04_Variables;

public class Currencies {

    public static void main(String[] args) {

        int dollar=1000;

        double lira= dollar * 9.53;
        double euro= dollar * 0.86;
        double jpy=dollar*130.26;
        double pound=dollar*0.81;
        double cad=dollar*1.30;
        double riyal=dollar*3.75;
        double rub=dollar*69;
        double sek=dollar*10.09;
        double nok=dollar*9.68;
        double dkk=dollar*7.04;
        //jpy, pound, peso, cad, riyal, rub ....

        System.out.println("lira = " + lira);
        System.out.println("dkk = " + dkk);
        System.out.println("nok = " + nok);
        System.out.println("sek = " + sek);
        System.out.println("rub = " + rub);
        System.out.println("riyal = " + riyal);
        System.out.println("cad = " + cad);
        System.out.println("pound = " + pound);
        System.out.println("jpy = " + jpy);
        System.out.println("euro = " + euro);
        System.out.println("dollar = " + dollar);

    }

}
